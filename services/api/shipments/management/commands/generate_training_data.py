CARGO_TYPES = [0, 0, 0, 0, 1, 1, 2, 3, 4, 4]  # General most common
RAINY_MONTHS = {3, 4, 5, 10, 11}


def sigmoid(x: float) -> float:
    """Logistic function — maps any real number to (0, 1)."""
    return 1.0 / (1.0 + math.exp(-x))


def compute_delay_probability(row: dict, rng: random.Random) -> float:
    """
    Probabilistic delay model calibrated for East Africa logistics.

    Coefficients derived from:
    - World Bank LPI regional benchmarks
    - TMEA (TradeMark East Africa) freight time studies
    - Kenya National Bureau of Statistics road transport surveys

    Returns a float in (0, 1) representing P(delay).
    """
    # Base risk from route characteristics
    score = (
          0.0018 * row["distance_km"]
        + 0.055  * row["estimated_hours"]
        - 0.000035 * row["weight_kg"]        # heavier = more planned = lower risk
        + 0.035  * row["hour_of_departure"]
        + 1.10   * row["has_customs_stop"]
        - 2.20   * row["carrier_on_time_rate"]
        + 0.50   * row["is_rainy_season"]
        - 0.30   * row["road_condition_score"]
        + 0.35   * row["border_crossings"]
        + 0.18   * row["cargo_type_encoded"]  # hazmat/perishable = higher risk
        + 0.12   * row["num_stops"]
        + 0.15   * row["is_weekend_departure"]
        + rng.gauss(0, 0.55)                 # stochastic noise
        - 3.8                                # intercept: calibrates to ~35% base rate
    )
    return sigmoid(score)


class Command(BaseCommand):
    """
    Django management command: generate_training_data
    Outputs a realistic synthetic ML dataset for East Africa freight delay prediction.
    """

    help = "Generate CargoTrack ML training dataset (open dataset for delay prediction)"

    def add_arguments(self, parser):
        parser.add_argument("--rows",  type=int,   default=15000,      help="Total rows to generate (default: 15000)")
        parser.add_argument("--seed",  type=int,   default=42,         help="Random seed for reproducibility (default: 42)")
        parser.add_argument("--out",   type=str,   default="ml_datasets", help="Output directory (default: ml_datasets/)")
        parser.add_argument("--splits",type=str,   default="0.8,0.1,0.1",help="Train/val/test split ratios (default: 0.8,0.1,0.1)")

    def handle(self, *args, **options):
        n_rows    = options["rows"]
        seed      = options["seed"]
        out_dir   = options["out"]
        splits    = [float(s) for s in options["splits"].split(",")]

        os.makedirs(out_dir, exist_ok=True)

        rng = random.Random(seed)
        self.stdout.write(f"Generating {n_rows:,} rows (seed={seed})…")

        # ── Generate rows ─────────────────────────────────────────────────────
        rows: list[dict] = []
        start_date = datetime(2022, 1, 1)

        for i in range(n_rows):
            # Sample a route
            origin, dest, dist, est_h, has_cust, border_x, road_score = rng.choice(ROUTES)

            # Add distance variance ±10%
            dist    = round(dist * rng.uniform(0.92, 1.08))
            est_h   = round(est_h * rng.uniform(0.90, 1.18), 1)

            # Cargo characteristics
            weight_kg    = round(rng.uniform(200, 28000), 1)
            cargo_type   = rng.choice(CARGO_TYPES)
            cargo_value  = rng.choice([0, 0, 1, 1, 1, 2])
            num_stops    = rng.randint(0, min(4, border_x + 2))

            # Carrier
            carrier, base_otr, otr_var = rng.choice(CARRIERS)
            carrier_otr = min(0.99, max(0.35, rng.gauss(base_otr, otr_var)))

            # Departure datetime
            dep_date      = start_date + timedelta(days=rng.randint(0, 365 * 3))
            hour_dep      = rng.randint(0, 23)
            dow           = dep_date.weekday()
            month         = dep_date.month
            is_rainy      = 1 if month in RAINY_MONTHS else 0
            is_weekend    = 1 if dow >= 5 else 0

            # Tracking event count
            n_events = rng.randint(1, min(16, max(2, int(dist / 100))))
            if rng.random() < 0.08:
                n_events = rng.randint(0, 2)  # sparse tracking = higher risk proxy

            row = {
                "distance_km":          dist,
                "estimated_hours":      est_h,
                "weight_kg":            weight_kg,
                "hour_of_departure":    hour_dep,
                "day_of_week":          dow,
                "month":                month,
                "route_origin":         origin,
                "route_destination":    dest,
                "num_tracking_events":  n_events,
                "has_customs_stop":     has_cust,
                "carrier_on_time_rate": round(carrier_otr, 4),
                "is_rainy_season":      is_rainy,
                "road_condition_score": road_score,
                "cargo_type_encoded":   cargo_type,
                "cargo_value_band":     cargo_value,
                "num_stops":            num_stops,
                "is_weekend_departure": is_weekend,
                "border_crossings":     border_x,
                "_carrier_name":        carrier,       # metadata (not a model feature)
                "_departure_date":      dep_date.date().isoformat(),
            }

            p_delay = compute_delay_probability(row, rng)
            row["delayed"] = 1 if rng.random() < p_delay else 0
            rows.append(row)

        # ── Summary stats ─────────────────────────────────────────────────────
        n_delayed = sum(r["delayed"] for r in rows)
        delay_rate = n_delayed / n_rows

        self.stdout.write(
            f"  Generated {n_rows:,} rows: {n_delayed:,} delayed ({delay_rate:.1%})"
        )

        # ── Split ─────────────────────────────────────────────────────────────
        rng.shuffle(rows)
        n_train = int(n_rows * splits[0])
        n_val   = int(n_rows * splits[1])
        train   = rows[:n_train]
        val     = rows[n_train:n_train + n_val]
        test    = rows[n_train + n_val:]

        # ── Write CSV helper ──────────────────────────────────────────────────
        # Columns for model training (excludes metadata _cols)
        model_cols = [k for k in rows[0] if not k.startswith("_")]
        all_cols   = list(rows[0].keys())

        def write_csv(path: str, data: list[dict], cols: list[str]) -> None:
            with open(path, "w", encoding="utf-8") as f:
                f.write(",".join(cols) + "\n")
                for r in data:
                    f.write(",".join(str(r[c]) for c in cols) + "\n")

        write_csv(os.path.join(out_dir, "cargotrack_delay_train.csv"),  train, model_cols)
        write_csv(os.path.join(out_dir, "cargotrack_delay_val.csv"),    val,   model_cols)
        write_csv(os.path.join(out_dir, "cargotrack_delay_test.csv"),   test,  model_cols)
        write_csv(os.path.join(out_dir, "cargotrack_delay_full.csv"),   rows,  all_cols)

        self.stdout.write(
            f"  Splits: train={len(train):,} / val={len(val):,} / test={len(test):,}"
        )

        # ── Descriptive statistics ────────────────────────────────────────────
        numeric_cols = [
            "distance_km", "estimated_hours", "weight_kg", "hour_of_departure",
            "carrier_on_time_rate", "num_tracking_events", "border_crossings",
        ]

        stats: dict = {"n_rows": n_rows, "delay_rate": round(delay_rate, 4)}
        for col in numeric_cols:
            values = [r[col] for r in rows]
            values.sort()
            n = len(values)
            mean = sum(values) / n
            variance = sum((v - mean) ** 2 for v in values) / n
            stats[col] = {
                "min":    round(values[0], 4),
                "max":    round(values[-1], 4),
                "mean":   round(mean, 4),
                "std":    round(variance ** 0.5, 4),
                "p25":    round(values[int(n * 0.25)], 4),
                "median": round(values[int(n * 0.50)], 4),
                "p75":    round(values[int(n * 0.75)], 4),
            }

        # Class balance per split
        stats["class_balance"] = {
            "train": {
                "delayed":     sum(r["delayed"] for r in train),
                "not_delayed": sum(1 - r["delayed"] for r in train),
            },
            "val": {
                "delayed":     sum(r["delayed"] for r in val),
                "not_delayed": sum(1 - r["delayed"] for r in val),
            },
            "test": {
                "delayed":     sum(r["delayed"] for r in test),
                "not_delayed": sum(1 - r["delayed"] for r in test),
            },
        }

        # Route coverage
        origins = {}
        for r in rows:
            k = f"{r['route_origin']} → {r['route_destination']}"
            origins[k] = origins.get(k, 0) + 1
        stats["top_routes"] = sorted(origins.items(), key=lambda x: -x[1])[:10]

        with open(os.path.join(out_dir, "cargotrack_delay_stats.json"), "w") as f:
            json.dump(stats, f, indent=2)

        # ── Schema ────────────────────────────────────────────────────────────
        # Stamp with generation metadata before writing
        FEATURE_SCHEMA["generated"] = datetime.utcnow().isoformat() + "Z"
        FEATURE_SCHEMA["n_rows"]    = n_rows
        FEATURE_SCHEMA["seed"]      = seed
        FEATURE_SCHEMA["splits"]    = {"train": splits[0], "val": splits[1], "test": splits[2]}
        FEATURE_SCHEMA["delay_rate"]= round(delay_rate, 4)

        with open(os.path.join(out_dir, "cargotrack_delay_schema.json"), "w") as f:
            json.dump(FEATURE_SCHEMA, f, indent=2, ensure_ascii=False)

        self.stdout.write(self.style.SUCCESS(
            f"\nDone. Files written to {os.path.abspath(out_dir)}/\n"
            f"  cargotrack_delay_train.csv   ({len(train):,} rows)\n"
            f"  cargotrack_delay_val.csv     ({len(val):,} rows)\n"
            f"  cargotrack_delay_test.csv    ({len(test):,} rows)\n"
            f"  cargotrack_delay_full.csv    ({n_rows:,} rows)\n"
            f"  cargotrack_delay_schema.json (feature documentation)\n"
            f"  cargotrack_delay_stats.json  (descriptive statistics)\n"
            f"\nDelay rate: {delay_rate:.1%}  |  Seed: {seed}"
        ))
